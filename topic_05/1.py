#1)	Гра з комп’ютером: камінь, ножиці, папір. Програма виконує запит від користувача на введення одного із значень ["stone", "scissor", "paper"]. 
#Наступним кроком, використовуючи модуль random, програма у випадковому порядку вибирає одне із значень ["stone", "scissor", "paper"]. 
#В залежності від умови, що камінь перемагає ножиці, ножиці перемагають папір, а папір перемагає камінь визначити переможця.
import random

# Можливі варіанти
choices = ["камінь", "ножиці", "папір"]

# Запит вводу від користувача
user_choice = input("Введіть ваш вибір (камінь, ножиці, папір): ").strip().lower()

# Перевіряємо коректність вводу
if user_choice not in choices:
    print("Неправильний вибір. Виберіть один із варіантів: камінь, ножиці, папір.")
else:
    # Випадковий вибір комп'ютера
    computer_choice = random.choice(choices)
    print(f"Комп'ютер обрав: {computer_choice}")

    # Визначаємо переможця
    if user_choice == computer_choice:
        print("Нічия!")
    elif (user_choice == "камінь" and computer_choice == "ножиці") or \
         (user_choice == "ножиці" and computer_choice == "папір") or \
         (user_choice == "папір" and computer_choice == "камінь"):
        print("Ви виграли!")
    else:
        print("Переміг комп'ютер!")
